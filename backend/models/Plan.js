import mongoose from 'mongoose';

const planSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      enum: ['Starter', 'Pro', 'Premium'],
    },
    price: {
      type: Number,
      required: true,
    },
    currency: {
      type: String,
      default: 'USDT',
    },
    referralCommission: {
      type: Number,
      required: true,
      // percentage of payment amount that referrer gets
    },
    description: {
      type: String,
    },
    features: [String],
    isActive: {
      type: Boolean,
      default: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  { timestamps: true }
);

export default mongoose.model('Plan', planSchema);
